namespace ExcelMerger
{
    /// <summary>
    /// Окно с длительной фоновой операцией. Пересборка окон при смене языка
    /// (<see cref="ShellContext"/>) занятое окно не закрывает: Close() показал бы
    /// модальный вопрос «Прервать операцию?» посреди переключения языка, а ответ «Да»
    /// ещё и отменил бы работу. Занятое окно остаётся на прежнем языке до перезапуска.
    /// </summary>
    internal interface IBusyAware
    {
        bool IsBusy { get; }
    }
}
